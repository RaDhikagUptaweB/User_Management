public class Emp {  
private int id;  
private String fname,lname,email,password,confirm_password,cell_nos,profession;  
public int getId() {  
    return id;  
}  
public void setId(int id) {  
    this.id = id;  
}  
public String getFirst_Name() {  
    return fname;  
}  
public void setFirst_Name(String fname) {  
    this.fname = fname;  
} 
public String getLast_Name() {  
    return lname;  
}  
public void setLast_Name(String lname) {  
    this.lname = lname;  
} 
public String getEmail() {  
    return email;  
}  
public void setEmail(String email) {  
    this.email = email;  
}  

public String getPassword() {  
    return password;  
}  
public void setPassword(String password) {  
    this.password = password;  
}  

public String getConfirm_Password() {  
    return confirm_password;  
}  
public void setConfirm_Password(String confirm_password) {  
    this.confirm_password = confirm_password;  
}
public String getCell_Nos() {  
    return cell_nos;  
}  
public void setCell_Nos(String cell_nos) {  
    this.cell_nos = cell_nos;  
}  


public String getProfession() {  
    return profession;  
}  
public void setProfession(String profession) {  
    this.profession = profession;  
}
 
  
}  